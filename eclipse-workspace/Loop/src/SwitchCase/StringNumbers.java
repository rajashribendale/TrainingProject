package SwitchCase;

public class StringNumbers {

	public static void main(String[] args) {
		 String s = "three";

		switch (s) {

		case "one":

			System.out.println("one");

			break;

		case "two":

			System.out.println("two");
			break;

		case "three":

			System.out.println("three");
			break;

		default:

			System.out.println("no match");
		}
	}
}
